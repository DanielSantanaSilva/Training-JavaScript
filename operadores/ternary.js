// ternário

//Usando if

const idadeMinima = 18;
const IdadeAmanda = 27;

if (IdadeAmanda >= idadeMinima) {
  console.log("Pode escolher qualquer bebida");
} else {
  console.log("Não pode beber bebida alcoólica");
}

//Pode escolher qualquer bebida

// Exemplo usando ternário:

const idadeLimite = 18;
const IdadeDaniel = 17;

console.log(
  IdadeDaniel >= idadeLimite
    ? "Pode escolher qualquer bebida"
    : "Não pode beber bebida alcoólica"
);

//Não pode beber bebida alcoólica

// Estrutura do ternário
// (condição ? true : false)
//O operador ternario tem três operadore em uma unica linha o da condição o de true(verdade) e do false(falso)
